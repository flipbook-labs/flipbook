local function useTailwind(light: string, dark: string?, isDarkTheme: boolean): Color3
	if isDarkTheme and dark and colors[dark] then
		return dark
	end

	if colors[light] then
		return light
	end

	return Color3.new(0, 0, 0)
end

return useTailwind
